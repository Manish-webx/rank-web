                    const faqItem = this.closest('.faq-item');
                    const allFaqItems = document.querySelectorAll('.faq-item');


                    allFaqItems.forEach(function (item) {
                        if (item !== faqItem) {
                            item.classList.remove('active');
                        }
                    });


                    faqItem.classList.toggle('active');
                });
            });
        });


